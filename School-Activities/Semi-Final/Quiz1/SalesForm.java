    private void PurchaseItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PurchaseItemActionPerformed
        // TODO add your handling code here:
        String input;
        int productQuantity;
        input = JOptionPane.showInputDialog(null,"Enter Quantity: ","Input",JOptionPane.QUESTION_MESSAGE);
        productQuantity = Integer.parseInt(input);
        prod.setProdPurchaseQuantity(productQuantity);
        QuantityTF.setText(prod.getProdPurchaseQuantity()+"");
        if (prod.getProdInitialQuantity() == 0){
            JOptionPane.showMessageDialog(null, "INSUFFICIENT STOCKS!!!", null, JOptionPane.ERROR_MESSAGE );
            
        } else if (prod.getProdPurchaseQuantity() > prod.getProdInitialQuantity()){
            JOptionPane.showMessageDialog(null, "INSUFFICIENT STOCKS!!!", null, JOptionPane.ERROR_MESSAGE );
        
        } else if (prod.getProdPurchaseQuantity() <= 0){
            JOptionPane.showMessageDialog(null, "INVALID INPUT!!!", null, JOptionPane.ERROR_MESSAGE );
            
        } else {
            double total = prod.getProdPrice() * productQuantity;
            JOptionPane.showMessageDialog(null,  "Product Name: " +prod.getProdName() + "\nProduct Price: " + prod.getProdPrice()+ 
                    "\nProduct Quantity: " + (int)prod.getProdPurchaseQuantity() +
                    "\nTotal Price: "+ total);
            double numberOfitems = Double.parseDouble(InitialQuantityTF.getText());
            numberOfitems = numberOfitems - prod.getProdPurchaseQuantity();
            InitialQuantityTF.setText(numberOfitems+"");
            
            prod.setProdInitialQuantity((int)numberOfitems);
            prod.addToPurchaseHistory((int)(prod.getProdPurchaseQuantity()));
            PurchaseHistoryTA.setText(prod.getPurchaseHistory());
        } 
    }//GEN-LAST:event_PurchaseItemActionPerformed

