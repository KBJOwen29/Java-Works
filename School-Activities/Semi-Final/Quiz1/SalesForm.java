    private void SetItemDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SetItemDetailsActionPerformed
        // TODO add your handling code here:
        String input;
        String productName;
        double productPrice;
        int productInitialQuantity;
        productName = JOptionPane.showInputDialog(null,"Enter Product Name: ","Input",JOptionPane.QUESTION_MESSAGE);
        prod.setProdName(productName);
        ProductNameTF.setText(prod.getProdName());
        
        input = JOptionPane.showInputDialog(null,"Enter Product Price: ","Input",JOptionPane.QUESTION_MESSAGE);
        productPrice = Double.parseDouble(input);
        prod.setProdPrice(productPrice);
        PriceTF.setText(prod.getProdPrice()+"");
        
        input = JOptionPane.showInputDialog(null,"Initial Number Of Items: ","Input",JOptionPane.QUESTION_MESSAGE);
        productInitialQuantity = Integer.parseInt(input);
        prod.setProdInitialQuantity(productInitialQuantity);
        InitialQuantityTF.setText((int)prod.getProdInitialQuantity()+"");
        
    }//GEN-LAST:event_SetItemDetailsActionPerformed

