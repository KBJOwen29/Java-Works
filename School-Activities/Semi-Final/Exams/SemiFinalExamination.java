    private void PizzaFlavorsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PizzaFlavorsActionPerformed
        // TODO add your handling code here:
        String[] FlavorSelection = new String[] { "Pepperoni", "Hawaiian", "Overload", "Mushroom" };

        if (PizzaFlavors.getSelectedItem() == FlavorSelection[0]){
            String input = JOptionPane.showInputDialog(null,"Pizza Flavor: Pepperoni" + "\nEnter number of orders: ");
            int quantity = Integer.parseInt(input);
            prod.setQuantity(quantity);
            Quantity_label.setText(input);
            prod.setFlavor("Pepperoni");
            
        } else if (PizzaFlavors.getSelectedItem() == FlavorSelection[1]){
            String input = JOptionPane.showInputDialog(null,"Pizza Flavor: Hawaiian" + "\nEnter number of orders: ");
            int quantity = Integer.parseInt(input);
            prod.setQuantity(quantity);
            Quantity_label.setText(input);
            prod.setFlavor("Hawaiian");

        } else if (PizzaFlavors.getSelectedItem() == FlavorSelection[2]){
            String input = JOptionPane.showInputDialog(null,"Pizza Flavor: OverLoad" + "\nEnter number of orders: ");
            int quantity = Integer.parseInt(input);
            prod.setQuantity(quantity);
            Quantity_label.setText(input);
            prod.setFlavor("Overload");

        } 
        
    }//GEN-LAST:event_PizzaFlavorsActionPerformed

