    private void PayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PayActionPerformed
        // TODO add your handling code here:
        double n = JOptionPane.showConfirmDialog(null, "Are there any Discount?");
        if (n == JOptionPane.NO_OPTION){
            String message = OrderList.getText();
            double total = prod.getTotalAmount();
            message = message + "\n" +"\nType of Customer: Regular" + "\nDiscount: 0.0" + "\nTotal Amount: " + total;
            OrderList.setText(message);
            String input = JOptionPane.showInputDialog(null,"Enter Cash: ");
            Double amount = Double.parseDouble(input);
            if (amount < prod.getTotalAmount()){
                JOptionPane.showMessageDialog(null,"INSUFFICIENT CASH", "PAYMENT" , JOptionPane.ERROR_MESSAGE);
            } else if (amount > prod.getTotalAmount()){
                message = OrderList.getText();
                message = message + "\n" + "Cash: "+ amount + "\nChange: " + (amount-total);
                OrderList.setText(message);
            }
        } else if (n == JOptionPane.YES_OPTION){
            JOptionPane.showOptionDialog(null, "Choose Customer", null, WIDTH, HEIGHT, null, null , "Student");
            String message = OrderList.getText();
            double total = prod.getTotalAmount();
            message = message + "\n" +"\nType of Customer: Regular" + "Discount: 0.0" + "Total Amount: " + total;
            OrderList.setText(message);
            String input = JOptionPane.showInputDialog(null,"Enter Cash: ");
            Double amount = Double.parseDouble(input);
            if (amount < prod.getTotalAmount()){
                JOptionPane.showMessageDialog(null,"INSUFFICIENT CASH", "PAYMENT" , JOptionPane.ERROR_MESSAGE);
            } else if (amount > prod.getTotalAmount()){
                message = OrderList.getText();
                message = message + "\n" + "Cash: "+ amount + "\nChange: " + (total-amount);
                OrderList.setText(message);
            }
        }
    }//GEN-LAST:event_PayActionPerformed

